// Test for PA (Pre-amplifier) command parsing
void test_radioCATHandler_parseRemoteResponse_PA() {
    setUp_RadioCATHandler();
    
    // Test PA1 command - Pre-amplifier ON
    radioCatHandler->parseMessage("PA1;");
    
    // Check that pre-amplifier was set to ON
    TEST_ASSERT_TRUE(radioManager->getState().preAmplifier);
    
    // Test PA0 command - Pre-amplifier OFF
    radioCatHandler->parseMessage("PA0;");
    
    // Check that pre-amplifier was set to OFF
    TEST_ASSERT_FALSE(radioManager->getState().preAmplifier);
    
    tearDown_RadioCATHandler();
}

// Test for FT (VFO selection) command parsing
void test_radioCATHandler_parseRemoteResponse_FT() {
    setUp_RadioCATHandler();
    
    // Test FT0 command - Select VFO A for TX
    radioCatHandler->parseMessage("FT0;");
    
    // Check that TX VFO was set to A (0)
    TEST_ASSERT_EQUAL(0, radioManager->getState().currentTxVfo.load());
    
    // Test FT1 command - Select VFO B for TX
    radioCatHandler->parseMessage("FT1;");
    
    // Check that TX VFO was set to B (1)
    TEST_ASSERT_EQUAL(1, radioManager->getState().currentTxVfo.load());
    
    tearDown_RadioCATHandler();
}

