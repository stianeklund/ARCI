// Test fixture
static std::unique_ptr<MockSerialHandler> mockRadioSerial;
static std::unique_ptr<MockSerialHandler> mockUsbSerial;
static RadioManager* radioManager;
static CommandDispatcher* commandDispatcher;
static CATHandler* radioCatHandler;

// Using separate setup/teardown to avoid conflicts with other test files
void setUp_RadioCATHandler() {
    mockRadioSerial = std::make_unique<MockSerialHandler>();
    mockUsbSerial = std::make_unique<MockSerialHandler>();

    radioManager = new RadioManager(*mockRadioSerial, *mockUsbSerial);
    
    commandDispatcher = new CommandDispatcher();
    
    // Register command handlers needed for tests
    auto frequencyHandler = std::make_unique<FrequencyVfoCommandHandler>();
    auto modeHandler = std::make_unique<ModeCommandHandler>();
    auto receiverProcessingHandler = std::make_unique<ReceiverProcessingCommandHandler>();
    auto statusInfoHandler = std::make_unique<StatusInfoCommandHandler>();
    auto interfaceSystemHandler = std::make_unique<InterfaceSystemCommandHandler>();
    auto transmitterHandler = std::make_unique<TransmitterCommandHandler>();
    auto gainLevelHandler = std::make_unique<GainLevelCommandHandler>();
    auto antennaHandler = std::make_unique<AntennaCommandHandler>();
    auto cwHandler = std::make_unique<CwCommandHandler>();
    auto memoryHandler = std::make_unique<MemoryCommandHandler>();
    auto scanHandler = std::make_unique<ScanCommandHandler>();
    auto toneSquelchHandler = std::make_unique<ToneSquelchCommandHandler>();
    auto audioEqualizerHandler = std::make_unique<AudioEqualizerCommandHandler>();
    auto visualScanHandler = std::make_unique<VisualScanCommandHandler>();
    auto menuConfigHandler = std::make_unique<MenuConfigCommandHandler>();
    auto voiceMessageHandler = std::make_unique<VoiceMessageCommandHandler>();
    
    commandDispatcher->registerHandler(std::move(frequencyHandler));
    commandDispatcher->registerHandler(std::move(modeHandler));
    commandDispatcher->registerHandler(std::move(receiverProcessingHandler));
    commandDispatcher->registerHandler(std::move(statusInfoHandler));
    commandDispatcher->registerHandler(std::move(interfaceSystemHandler));
    commandDispatcher->registerHandler(std::move(transmitterHandler));
    commandDispatcher->registerHandler(std::move(gainLevelHandler));
    commandDispatcher->registerHandler(std::move(antennaHandler));
    commandDispatcher->registerHandler(std::move(cwHandler));
    commandDispatcher->registerHandler(std::move(memoryHandler));
    commandDispatcher->registerHandler(std::move(scanHandler));
    commandDispatcher->registerHandler(std::move(toneSquelchHandler));
    commandDispatcher->registerHandler(std::move(audioEqualizerHandler));
    commandDispatcher->registerHandler(std::move(visualScanHandler));
    commandDispatcher->registerHandler(std::move(menuConfigHandler));
    commandDispatcher->registerHandler(std::move(voiceMessageHandler));
    
    radioCatHandler = new CATHandler(*commandDispatcher, *radioManager,
        *mockRadioSerial, *mockUsbSerial, CommandSource::Remote);

    radioManager->setRadioConnected(true);
}

void tearDown_RadioCATHandler() {
    delete radioCatHandler;
    delete commandDispatcher;
    delete radioManager;
    mockRadioSerial.reset();
    mockUsbSerial.reset();
}

