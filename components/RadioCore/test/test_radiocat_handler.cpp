void test_radioCATHandler_parseRemoteResponse_IF() {
    setUp_RadioCATHandler();
    
    // Use the helper to create a realistic IF response with specific values
    const std::string full_if_response = cat::ParserUtils::buildIFResponse(
        14074000,     // VFO A frequency
        100,          // RIT/XIT offset  
        true,         // RIT on
        true,         // XIT on
        5,            // Memory channel 5
        true,         // TX on
        4,            // Mode 4: FM
        true,         // Split on
        0,            // Tone disabled
        0             // Tone number
    );
    
    // For backwards compatibility with the old fixed string test, also validate that format
    // const char* full_if_response = "IF00014074000xxxxx 01001100514xx1;";

    radioCatHandler->parseMessage(full_if_response.c_str());

    TEST_ASSERT_EQUAL_UINT32(14074000, static_cast<uint32_t>(radioManager->getState().vfoAFrequency.load()));
    TEST_ASSERT_EQUAL(100, radioManager->getState().ritXitOffset.load());
    TEST_ASSERT_TRUE(radioManager->getState().ritOn.load());
    TEST_ASSERT_TRUE(radioManager->getState().xitOn.load());
    TEST_ASSERT_EQUAL(5, radioManager->getState().memoryChannel.load());
    TEST_ASSERT_TRUE(radioManager->getState().isTx.load());
    TEST_ASSERT_EQUAL(4, radioManager->getState().mode.load());
    TEST_ASSERT_TRUE(radioManager->getState().split.load());

    tearDown_RadioCATHandler();
}

